# ---------------------------------------------------------------------------
# Pydantic models
# ---------------------------------------------------------------------------
class GenerateRequest(BaseModel):
    text: str = Field(..., min_length=10, max_length=4000)
    type: str = Field(..., pattern="^(image|video)$")

class CancelResponse(BaseModel):
    task_id: str
    message: str

# ---------------------------------------------------------------------------
# Utilities
# ---------------------------------------------------------------------------
def task_update(task_id: str, **kw) -> None:
    if task_id not in tasks:
        return
    tasks[task_id].update(kw)
    log.info("Task %s  status=%-12s  progress=%3d%%  msg=%s",
             task_id[:8], tasks[task_id].get("status", ""),
             tasks[task_id].get("progress", 0), kw.get("message", ""))

def is_cancelled(tid: str) -> bool:
    return tasks.get(tid, {}).get("cancelled", False)

def _ltx_headers() -> dict:
    if not LTX_API_KEY:
        raise RuntimeError("LTX_API_KEY not set in .env — get your key at https://console.ltx.video/")
    return {"Authorization": f"Bearer {LTX_API_KEY}", "Content-Type": "application/json"}

def _ffmpeg_available() -> bool:
    try:
        subprocess.run(["ffmpeg", "-version"], capture_output=True, check=True, timeout=5)
        return True
    except Exception:
        return False

# ---------------------------------------------------------------------------
# Prompt enhancement
# ---------------------------------------------------------------------------
async def enhance_prompt(text: str, mode: str) -> str:
    if GROQ_API_KEY:
        return await _enhance_with_groq(text, mode)
    return _keyword_prompt(text, mode)

async def _enhance_with_groq(text: str, mode: str) -> str:
    """
    Uses Groq's free tier API (Llama 3.3 70B) to generate a high-quality
    visual prompt from the selected article text.
    Free tier: ~14,400 requests/day. No credit card needed.
    Docs: https://console.groq.com/docs/openai
    """
    instruction = (
        "Write ONE concise video generation prompt (max 150 words) describing MOTION and CAMERA "
        "MOVEMENT for a video representing this scientific text. Describe what moves, how it moves, "
        "and camera behavior (e.g. 'slow push-in', 'pan across'). "
        "Style: cinematic, 4K, dramatic lighting, photorealistic. "
        "Output ONLY the prompt — no preamble, no markdown, no quotes."
        if mode == "video" else
        "Write ONE concise image generation prompt (max 150 words) that visually represents "
        "this scientific text. Include subject, lighting, style, and composition. "
        "Style: scientific illustration, photorealistic, bright white background, sharp focus, 4K. "
        "Output ONLY the prompt — no preamble, no markdown, no quotes."
    )
    payload = {
        "model": "llama-3.3-70b-versatile",   # free on Groq
        "max_tokens": 250,
        "temperature": 0.4,
        "messages": [
            {"role": "system", "content": "You are an expert visual prompt engineer for AI video and image generation."},
            {"role": "user",   "content": f"{instruction}\n\nScientific text:\n{text[:1500]}"},
        ],
    }
    for attempt in range(1, 4):
        try:
            async with httpx.AsyncClient(timeout=HTTP_TIMEOUT) as hc:
                resp = await hc.post(
                    "https://api.groq.com/openai/v1/chat/completions",
                    json=payload,
                    headers={"Authorization": f"Bearer {GROQ_API_KEY}",
                             "Content-Type": "application/json"},
                )
                resp.raise_for_status()
                content = resp.json()["choices"][0]["message"]["content"].strip()
                content = re.sub(r"^```[^\n]*\n?", "", content)
                content = re.sub(r"\n?```$", "", content).strip()
                content = content.strip('"\'')
                log.info("Groq prompt (attempt %d): %s...", attempt, content[:80])
                return content
        except Exception as exc:
            log.warning("Groq attempt %d/3: %s", attempt, exc)
            if attempt < 3:
                await asyncio.sleep(3 * attempt)
    return _keyword_prompt(text, mode)

def _keyword_prompt(text: str, mode: str) -> str:
    stop = {"the","a","an","and","or","but","in","on","at","to","for","of","with","by","from",
            "is","are","was","were","be","been","have","has","had","do","does","did","will",
            "would","could","should","may","might","this","that","these","those","it","its",
            "they","we","our","which","who","what","when","where","how","all","each","every",
            "both","few","more","most","other","some","such","no","not","only","same","so",
            "than","too","very","just","also","about","into","through","using"}
    words = re.findall(r"\b[A-Za-z][A-Za-z\-]{3,}\b", text)
    kw, seen = [], set()
    for w in words:
        lw = w.lower()
        if lw not in stop and lw not in seen:
            seen.add(lw); kw.append(w)
        if len(kw) >= 10: break
    core = ", ".join(kw[:8]) if kw else text[:120]
    if mode == "video":
        return (f"Scientific visualization of {core}. Camera slowly pushes in, "
                "particles and structures animate fluidly, cinematic lighting, vibrant colors, 4K. "
                "Synchronized ambient audio and subtle sound effects.")
    return (f"Detailed scientific illustration of {core}. "
            "Bright background, sharp focus, vibrant colors, photorealistic, 4K.")

# ---------------------------------------------------------------------------
# LTX-2 API call — synchronous, returns raw MP4 bytes
# ---------------------------------------------------------------------------
async def call_ltx_video(prompt: str, model: str, resolution: str, duration: int,
                          fps: int, task_id: str, progress_start: int, progress_end: int) -> bytes:
    """
    POST to https://api.ltx.video/v1/text-to-video
    The LTX-2 API is synchronous — the HTTP response body IS the video file (MP4).
    No polling or webhooks needed.
    """
    payload = {
        "prompt":         prompt,
        "model":          model,
        "duration":       duration,
        "resolution":     resolution,
        "fps":            fps,
        "generate_audio": True,
    }
    log.info("LTX-2 request: model=%s  res=%s  dur=%ds  fps=%d", model, resolution, duration, fps)

    # Animate progress bar while the synchronous request is in flight
    async def _progress_ticker():
        value = progress_start
        while value < progress_end - 5:
            await asyncio.sleep(5)
            value = min(value + 2, progress_end - 5)
            if task_id in tasks and not is_cancelled(task_id):
                tasks[task_id]["progress"] = value

    ticker = asyncio.create_task(_progress_ticker())
    try:
        async with httpx.AsyncClient(timeout=HTTP_TIMEOUT) as hc:
            resp = await hc.post(f"{LTX_API_BASE}/text-to-video", json=payload, headers=_ltx_headers())

        # Friendly error messages
        if resp.status_code == 401:
            raise RuntimeError("Invalid LTX API key — check LTX_API_KEY in .env")
        if resp.status_code == 402:
            raise RuntimeError("Insufficient LTX credits — top up at console.ltx.video")
        if resp.status_code == 422:
            raise RuntimeError("Content filtered by LTX safety system — try a different prompt")
        if resp.status_code == 429:
            raise RuntimeError("LTX rate limit reached — wait a moment and retry")
        if resp.status_code >= 400:
            try:
                err = resp.json().get("error", {}).get("message", resp.text[:200])
            except Exception:
                err = resp.text[:200]
            raise RuntimeError(f"LTX API error {resp.status_code}: {err}")

        data = resp.content
        if len(data) < 10_000:
            try:
                body = resp.json()
                raise RuntimeError(f"LTX returned unexpected response: {body}")
            except Exception:
                raise RuntimeError(
                    f"LTX returned only {len(data)} bytes — expected MP4. "
                    "Verify your API key and model/resolution/duration combination."
                )

        log.info("LTX-2 response: %d bytes", len(data))
        return data
    finally:
        ticker.cancel()
        try: await ticker
        except asyncio.CancelledError: pass

# ---------------------------------------------------------------------------
# Extract first frame as JPEG using ffmpeg
# ---------------------------------------------------------------------------
def extract_first_frame(mp4_path: str, out_path: str) -> None:
    cmd = ["ffmpeg", "-y", "-i", mp4_path, "-vframes", "1", "-q:v", "2", out_path]
    r = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
    if r.returncode != 0:
        raise RuntimeError(f"ffmpeg frame extraction failed:\n{r.stderr[-400:]}")
    log.info("Frame extracted: %s", out_path)

# ---------------------------------------------------------------------------
# Burn text overlay onto video using ffmpeg drawtext filter
# ---------------------------------------------------------------------------
def burn_text_overlay(in_path: str, out_path: str, text: str) -> None:
    """
    Renders a clean caption bar at the bottom of the video.
    - White text, semi-transparent dark bar background
    - Auto word-wraps at ~60 chars per line
    - Uses the built-in ffmpeg monospace font (no external font file needed)
    """
    # Trim to max ~200 chars so it fits without overflow
    caption = text.strip()[:200].replace("'", "\\'").replace(":", "\\:").replace("\\", "\\\\")

    # drawtext filter: dark translucent box + white text, bottom-centred
    drawtext = (
        "drawtext="
        "text='{caption}':"
        "x=(w-text_w)/2:"
        "y=h-th-24:"
        "fontsize=28:"
        "fontcolor=white:"
        "box=1:"
        "boxcolor=black@0.55:"
        "boxborderw=10:"
        "line_spacing=6:"
        "fix_bounds=1"
    ).format(caption=caption)

    cmd = [
        "ffmpeg", "-y",
        "-i", in_path,
        "-vf", drawtext,
        "-c:v", "libx264", "-preset", "fast", "-crf", "20",
        "-c:a", "copy",
        "-movflags", "+faststart",
        out_path,
    ]
    r = subprocess.run(cmd, capture_output=True, text=True, timeout=300)
    if r.returncode != 0:
        raise RuntimeError(f"ffmpeg text overlay failed:\n{r.stderr[-600:]}")
    log.info("Text overlay burned: %s", out_path)

# ---------------------------------------------------------------------------
# Background task: IMAGE
# ---------------------------------------------------------------------------
async def _task_image(task_id: str, text: str) -> None:
    tmp_video = TEMP_DIR / f"{task_id}_tmp.mp4"
    try:
        task_update(task_id, status="running", progress=8, message="Crafting visual prompt...")
        prompt = await enhance_prompt(text, "image")
        if is_cancelled(task_id):
            task_update(task_id, status="cancelled", progress=0, message="Cancelled."); return

        task_update(task_id, progress=18, message=f"Generating image via LTX-2 ({IMAGE_MODEL})...")
        video_bytes = await call_ltx_video(
            prompt=prompt, model=IMAGE_MODEL, resolution=IMAGE_RESOLUTION,
            duration=IMAGE_DURATION, fps=25, task_id=task_id,
            progress_start=18, progress_end=85,
        )
        if is_cancelled(task_id):
            task_update(task_id, status="cancelled", progress=0, message="Cancelled."); return

        task_update(task_id, progress=86, message="Extracting image frame...")
        tmp_video.write_bytes(video_bytes)

        if _ffmpeg_available():
            fname    = f"{task_id}.jpg"
            out_path = IMAGES_DIR / fname
            extract_first_frame(str(tmp_video), str(out_path))
        else:
            # No ffmpeg — save first frame via Python (basic, lower quality)
            log.warning("ffmpeg not found — saving raw video bytes as fallback")
            fname    = f"{task_id}.mp4"
            out_path = IMAGES_DIR / fname
            out_path.write_bytes(video_bytes)

        task_update(task_id, status="completed", progress=100, message="Image generated!",
                    result={"type": "image", "url": f"/generated/images/{fname}",
                            "filename": fname, "revised_prompt": prompt, "model": IMAGE_MODEL})
    except asyncio.CancelledError:
        task_update(task_id, status="cancelled", progress=0, message="Cancelled.")
    except Exception as exc:
        log.exception("Image task %s failed", task_id)
        task_update(task_id, status="error", progress=0, message=str(exc), error=str(exc))
    finally:
        tmp_video.unlink(missing_ok=True)

# ---------------------------------------------------------------------------
# Background task: VIDEO
# ---------------------------------------------------------------------------
async def _task_video(task_id: str, text: str) -> None:
    tmp_raw = TEMP_DIR / f"{task_id}_raw.mp4"
    try:
        task_update(task_id, status="running", progress=5, message="Crafting cinematic prompt...")
        prompt = await enhance_prompt(text, "video")
        if is_cancelled(task_id):
            task_update(task_id, status="cancelled", progress=0, message="Cancelled."); return

        task_update(task_id, progress=15,
                    message=f"Generating {VIDEO_DURATION}s video with {VIDEO_MODEL}...")
        video_bytes = await call_ltx_video(
            prompt=prompt, model=VIDEO_MODEL, resolution=VIDEO_RESOLUTION,
            duration=VIDEO_DURATION, fps=VIDEO_FPS, task_id=task_id,
            progress_start=15, progress_end=88,
        )
        if is_cancelled(task_id):
            task_update(task_id, status="cancelled", progress=0, message="Cancelled."); return

        fname    = f"{task_id}.mp4"
        out_path = VIDEOS_DIR / fname

        if _ffmpeg_available():
            # Burn the selected article text as a caption overlay
            task_update(task_id, progress=90, message="Burning text overlay...")
            tmp_raw.write_bytes(video_bytes)
            loop = asyncio.get_event_loop()
            await loop.run_in_executor(
                None, burn_text_overlay,
                str(tmp_raw), str(out_path),
                # Use first 180 chars of the original selected text as caption
                text[:180].strip()
            )
            log.info("Video with overlay saved: %s", fname)
        else:
            # No ffmpeg — save raw video without overlay
            log.warning("ffmpeg not found — saving video without text overlay")
            out_path.write_bytes(video_bytes)

        log.info("Video saved: %s  %d bytes", fname, out_path.stat().st_size)

        task_update(task_id, status="completed", progress=100, message="Video generated!",
                    result={"type": "video", "url": f"/generated/videos/{fname}",
                            "filename": fname, "revised_prompt": prompt, "model": VIDEO_MODEL,
                            "duration": VIDEO_DURATION, "resolution": VIDEO_RESOLUTION})
    except asyncio.CancelledError:
        task_update(task_id, status="cancelled", progress=0, message="Cancelled.")
    except Exception as exc:
        log.exception("Video task %s failed", task_id)
        task_update(task_id, status="error", progress=0, message=str(exc), error=str(exc))
    finally:
        tmp_raw.unlink(missing_ok=True)

