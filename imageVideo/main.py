app = FastAPI(title="Lumina AI", version="9.0.0", lifespan=lifespan)
app.add_middleware(CORSMiddleware, allow_origins=["*"], allow_methods=["*"], allow_headers=["*"])

@app.post("/api/generate")
async def generate(req: GenerateRequest, bg: BackgroundTasks):
    if not LTX_API_KEY:
        raise HTTPException(503, detail="LTX_API_KEY not configured. Add it to your .env file.")
    task_id = str(uuid.uuid4())
    tasks[task_id] = {"task_id": task_id, "type": req.type,
                       "text": req.text[:200] + ("..." if len(req.text) > 200 else ""),
                       "status": "pending", "progress": 0, "message": "Queued...",
                       "result": None, "error": None, "cancelled": False,
                       "created_at": time.time()}
    if req.type == "image":
        bg.add_task(_task_image, task_id, req.text)
    else:
        bg.add_task(_task_video, task_id, req.text)
    return {"task_id": task_id, "type": req.type}

@app.get("/api/status/{task_id}")
async def get_status(task_id: str):
    if task_id not in tasks:
        raise HTTPException(404, "Task not found.")
    return tasks[task_id]

@app.post("/api/cancel/{task_id}", response_model=CancelResponse)
async def cancel_task(task_id: str):
    """
    Sets the cancelled flag. Because LTX-2 is synchronous, the in-flight HTTP
    request can't be stopped mid-flight, but the task will not save its result.
    """
    if task_id not in tasks:
        raise HTTPException(404, "Task not found.")
    t = tasks[task_id]
    if t["status"] in ("completed", "cancelled", "error"):
        return CancelResponse(task_id=task_id, message=f"Already finished: {t['status']}")
    tasks[task_id]["cancelled"] = True
    return CancelResponse(task_id=task_id, message="Cancel signal sent.")

@app.get("/api/tasks")
async def list_tasks():
    return sorted(tasks.values(), key=lambda t: t.get("created_at", 0), reverse=True)[:100]

@app.delete("/api/tasks/{task_id}")
async def delete_task(task_id: str):
    if task_id not in tasks:
        raise HTTPException(404, "Task not found.")
    t = tasks.pop(task_id)
    if t.get("result"):
        for d in [IMAGES_DIR, VIDEOS_DIR]:
            fp = d / t["result"].get("filename", "")
            if fp.exists(): fp.unlink()
    return {"deleted": task_id}

@app.get("/api/health")
async def health():
    return {"status": "ok", "ltx_key_set": bool(LTX_API_KEY),
            "groq_key": bool(GROQ_API_KEY), "video_model": VIDEO_MODEL,
            "video_resolution": VIDEO_RESOLUTION, "video_duration": VIDEO_DURATION,
            "video_fps": VIDEO_FPS, "ffmpeg": _ffmpeg_available(),
            "active_tasks": sum(1 for t in tasks.values() if t["status"] == "running"),
            "total_tasks": len(tasks)}

@app.get("/", include_in_schema=False)
async def serve_root():
    idx = STATIC_DIR / "index.html"
    if idx.exists():
        return FileResponse(str(idx), media_type="text/html")
    return JSONResponse({"error": "index.html missing", "hint": "Copy index.html to static/"}, 404)

app.mount("/generated", StaticFiles(directory=str(GENERATED_DIR)), name="generated")
app.mount("/",          StaticFiles(directory=str(STATIC_DIR), html=True), name="static")

